#include <stdio.h>

int main(){
	
	printf("format specifier\n");
	
	printf("integer value: %d \n", 10);
	printf("real numbers: %f \n", 3.1415926);
	printf("real numbers with two decimals: %.2f \n", 3.1415926);
	printf("text data with one character: %c \n", 'a');
	printf("text data: %s \n", "text");
	
	system("pause");
	return 0;
}
